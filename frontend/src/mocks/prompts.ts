import type { Prompt, PromptVersion } from "../types/prompt";

function makeVersion(
  id: string,
  promptId: string,
  version: number,
  content: string,
  changelog: string,
  createdBy: string,
  createdAt: string,
): PromptVersion {
  const variables = content.match(/\{\{(\w+)\}\}/g)?.map((v) => v.slice(2, -2)) ?? [];
  return { id, promptId, version, content, variables, changelog, createdBy, createdAt };
}

