export function buildCollections(): CollectionInfo[] {
  const counts: Record<KnowledgeCollection, number> = {
    engineering: 0,
    product: 0,
    hr: 0,
    legal: 0,
    marketing: 0,
    "customer-support": 0,
    security: 0,
    operations: 0,
  };

  for (const item of mockKnowledgeItems) {
    if (item.status === "active") {
      counts[item.collection]++;
    }
  }

  return (Object.keys(collectionMeta) as KnowledgeCollection[]).map((id) => ({
    id,
    name: collectionMeta[id].name,
    description: collectionMeta[id].description,
    icon: collectionMeta[id].icon,
    itemCount: counts[id],
  }));
}
